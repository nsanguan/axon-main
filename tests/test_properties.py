"""Property-based tests for Axon planning invariants.

Uses Hypothesis to verify that key mathematical properties always hold
regardless of input values:

  1. Utility monotonicity — adding more score on any dimension cannot
     decrease the overall utility score.
  2. Supply bounds — the total allocated quantity across all allocations
     for a given supply can never exceed the supply's available quantity.
  3. Demand completeness — every demand is either allocated or explicitly
     marked as deferred; none are silently dropped.
  4. BusinessWeights validation — weights that sum to 1.0 pass, others fail.
  5. Utility engine consistency — global_utility >= sum of any subset.
"""

from __future__ import annotations

from decimal import Decimal
from typing import Any
from uuid import uuid4

import pytest
from hypothesis import assume, given, settings
from hypothesis import strategies as st

from axon.core.schema import (
    AgentProposal,
    Allocation,
    Demand,
    EntityRef,
    Period,
    ProposalStatus,
    Supply,
)
from axon.orchestrator.conflict_resolver import (
    BusinessWeights,
    ConflictResolver,
    NegotiationConfig,
    global_utility,
    utility_score,
)


